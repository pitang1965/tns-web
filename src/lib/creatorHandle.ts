export function getCreatorHandle(ownerId: string | undefined | null): string {
  // owner.id を持たない旧データのフォールバック
  if (!ownerId) {
    return '名無しさん';
  }

  const hash = createHash('sha256').update(ownerId).digest('hex');

  // ハッシュの別々の区間から接頭辞・動物のインデックスを決定的に算出
  const prefixIndex = parseInt(hash.slice(0, 8), 16) % PREFIXES.length;
  const animalIndex = parseInt(hash.slice(8, 16), 16) % ANIMALS.length;

  return `${PREFIXES[prefixIndex]}${ANIMALS[animalIndex]}さん`;
}
